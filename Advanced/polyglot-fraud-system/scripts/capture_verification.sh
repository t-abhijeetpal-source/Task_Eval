#!/usr/bin/env bash
# A3 verification capture (A3-004 / constitution rule #4: "every metric claim
# requires captured command output"). Runs all verification layers, saves raw
# logs under artifacts/repro/, and regenerates VERIFICATION_RESULTS.md from the
# ACTUAL counts parsed out of those logs — never hand-edited numbers.
set -uo pipefail

A3="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"
REPRO="$A3/artifacts/repro"
mkdir -p "$REPRO"
cd "$A3"

PYBIN="$A3/fastapi-service/.venv/bin/python"; [ -x "$PYBIN" ] || PYBIN="$(command -v python3)"
PYVER="$("$PYBIN" --version 2>&1 | awk '{print $2}')"
NODEVER="$(node --version 2>/dev/null | tr -d v)"
RUSTVER="$(rustc --version 2>/dev/null | awk '{print $2}')"
STAMP="$(date -u +%Y-%m-%dT%H:%M:%SZ)"

echo "== [1/5] Rust: cargo build --release + cargo test =="
( cd rust-engine && cargo build --release && cargo test ) >"$REPRO/rust.log" 2>&1
RUST_RC=$?
# Rust prints several "N passed" lines (lib, bin, integration, doctests); the
# real engine tests live in the integration binary, so take the MAX count.
RUST_COUNT=$(grep -Eo '[0-9]+ passed' "$REPRO/rust.log" | awk '{print $1}' | sort -rn | head -1)

echo "== [2/5] FastAPI: pytest -q =="
( cd fastapi-service && "$PYBIN" -m pytest -q ) >"$REPRO/pytest.log" 2>&1
PYTEST_RC=$?
PYTEST_COUNT=$(grep -Eo '[0-9]+ passed' "$REPRO/pytest.log" | head -1 | awk '{print $1}')

echo "== [3/5] Node: npm test =="
( cd node-worker && npm test ) >"$REPRO/node.log" 2>&1
NODE_RC=$?
NODE_COUNT=$(grep -Eo 'Tests:[[:space:]]+[0-9]+ passed' "$REPRO/node.log" | grep -Eo '[0-9]+ passed' | head -1 | awk '{print $1}')

echo "== [4/5] Contract conformance (4 canonical vectors through the engine) =="
bash scripts/contract_conformance.sh >"$REPRO/conformance.log" 2>&1
CONF_RC=$?
CONF_COUNT=$(grep -Eo '[0-9]+ passed' "$REPRO/conformance.log" | head -1 | awk '{print $1}')

echo "== [5/5] End-to-end integration =="
bash integration-tests/run_integration.sh >"$REPRO/integration.log" 2>&1
INT_RC=$?
INT_PASS=$(grep -c 'PASS' "$REPRO/integration.log")
INT_FAIL=$(grep -Ec '  FAIL|INTEGRATION: FAIL' "$REPRO/integration.log")

# Count the 4 canonical-vector PASS lines specifically (they have score=/risk=).
INT_VEC=$(grep -Ec 'score=.*risk=.*status=scored.*PASS' "$REPRO/integration.log")

OVERALL=0
for rc in "$RUST_RC" "$PYTEST_RC" "$NODE_RC" "$CONF_RC" "$INT_RC"; do
  [ "$rc" -ne 0 ] && OVERALL=1
done

cat > "$A3/VERIFICATION_RESULTS.md" <<EOF
# A3 — Verification Run Results

> Real, executed output — regenerated by \`scripts/capture_verification.sh\`, NOT hand-edited.
> Raw logs: \`artifacts/repro/{rust,pytest,node,conformance,integration}.log\`.
> Env: Python ${PYVER} (service venv) · Node ${NODEVER} · Rust ${RUSTVER}. Captured: ${STAMP}.

## Status: $( [ "$OVERALL" = 0 ] && echo '✅ VERIFIED' || echo '❌ FAILED' ) (Rust ${RUST_COUNT:-?} · FastAPI ${PYTEST_COUNT:-?} · Node ${NODE_COUNT:-?} · Conformance ${CONF_COUNT:-?}/4 · Integration ${INT_VEC:-?}/4)

| Layer | Command | Result |
|---|---|---|
| Rust engine | \`cargo test\` | **${RUST_COUNT:-?} passed** (rc=$RUST_RC) |
| FastAPI | \`pytest -q\` | **${PYTEST_COUNT:-?} passed** (rc=$PYTEST_RC) |
| Node worker | \`npm test\` | **${NODE_COUNT:-?} passed** (rc=$NODE_RC) |
| Contract conformance | \`scripts/contract_conformance.sh\` | **${CONF_COUNT:-?}/4 vectors** (rc=$CONF_RC) |
| End-to-end | \`integration-tests/run_integration.sh\` | **${INT_VEC:-?}/4 PASS** (rc=$INT_RC) |

---

## 1. Rust — \`cargo test\`
\`\`\`text
$(grep -E 'running [0-9]+ tests|test result:' "$REPRO/rust.log" | head -8)
\`\`\`

## 2. FastAPI — \`pytest -q\`
\`\`\`text
$(tail -1 "$REPRO/pytest.log")
\`\`\`

## 3. Node worker — \`npm test\`
\`\`\`text
$(grep -E 'Tests:|Test Suites:' "$REPRO/node.log")
\`\`\`

## 4. Contract conformance — \`scripts/contract_conformance.sh\`
\`\`\`text
$(cat "$REPRO/conformance.log")
\`\`\`

## 5. End-to-end integration — \`run_integration.sh\`
\`\`\`text
$(grep -E 'queued files|score=|INTEGRATION:' "$REPRO/integration.log")
\`\`\`

---

## How to reproduce
\`\`\`bash
cd "Advanced/polyglot-fraud-system"
bash scripts/capture_verification.sh   # regenerates this file + artifacts/repro/
# or run layers individually:
( cd rust-engine && cargo build --release && cargo test )
( cd fastapi-service && python3 -m venv .venv && . .venv/bin/activate && pip install -r requirements.txt && pytest -q )
( cd node-worker && npm install && npm test )
bash scripts/contract_conformance.sh
A3_INTERNAL_TOKEN=\$(openssl rand -hex 16) bash integration-tests/run_integration.sh
\`\`\`
EOF

echo
echo "WROTE VERIFICATION_RESULTS.md  (rust=${RUST_COUNT} pytest=${PYTEST_COUNT} node=${NODE_COUNT} conf=${CONF_COUNT}/4 int=${INT_VEC}/4)"
echo "OVERALL: $( [ "$OVERALL" = 0 ] && echo PASS || echo FAIL )"
exit $OVERALL
