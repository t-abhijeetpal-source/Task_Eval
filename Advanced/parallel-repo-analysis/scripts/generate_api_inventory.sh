#!/usr/bin/env bash
# generate_api_inventory.sh — emit a structured YAML inventory of the outbound API surface.
# When TARGET_REPO is set, it greps live (Retrofit interfaces + verb annotations + Flutter routes).
# Otherwise it emits the verified/inferred inventory from the committed reports (clearly labeled).
# Output: docs/agent-analysis/A1_api_inventory.yaml
set -uo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
A1_ROOT="$(cd "$SCRIPT_DIR/.." && pwd)"
OUT="$A1_ROOT/docs/agent-analysis/A1_api_inventory.yaml"

if [ -f "$A1_ROOT/.env" ]; then set -a; . "$A1_ROOT/.env"; set +a; fi
TARGET_REPO="${TARGET_REPO:-}"

if [ -n "$TARGET_REPO" ] && [ -d "$TARGET_REPO" ]; then
  T="$TARGET_REPO"
  SRC="live (grepped from \$TARGET_REPO)"
  COMMIT="$(git -C "$T" rev-parse HEAD 2>/dev/null || echo unknown)"
  IFACES=$(grep -rlE '@(GET|POST|PUT|DELETE)' "$T/equity_sdk/src/main" 2>/dev/null | wc -l | tr -d ' ')
  GET=$(grep -rohE '@GET' "$T/equity_sdk/src/main" 2>/dev/null | wc -l | tr -d ' ')
  POST=$(grep -rohE '@POST' "$T/equity_sdk/src/main" 2>/dev/null | wc -l | tr -d ' ')
  PUT=$(grep -rohE '@PUT' "$T/equity_sdk/src/main" 2>/dev/null | wc -l | tr -d ' ')
  DEL=$(grep -rohE '@DELETE' "$T/equity_sdk/src/main" 2>/dev/null | wc -l | tr -d ' ')
  URL=$(grep -rohE '@Url' "$T/equity_sdk/src/main" 2>/dev/null | wc -l | tr -d ' ')
else
  SRC="from committed reports (TARGET_REPO unset — values are the verified/inferred report counts)"
  COMMIT="unknown"
  IFACES=78; GET=247; POST=112; PUT=14; DEL=6; URL=374
fi

cat > "$OUT" <<EOF
# A1 — Outbound API inventory (structured)
# source: $SRC
# generated by scripts/generate_api_inventory.sh
target_commit: $COMMIT
surface: outbound-only   # client app; no server endpoints (VERIFIED)

retrofit:
  equity_sdk:
    interface_files: $IFACES        # VERIFIED
    base_app_interface_files: 1     # BaseService.kt (VERIFIED)
    endpoint_methods_estimate: 363  # INFERRED (annotation-derived, incl. dynamic @Url)
    verb_annotations:
      GET: $GET
      POST: $POST
      PUT: $PUT
      DELETE: $DEL
    url_dynamic: $URL               # @Url dynamic-path params (dominant pattern)
    static_literal_paths: 5         # VERIFIED
  auth_injection:
    interceptor: CoreNetworkInterceptor   # library/ (VERIFIED)
    headers: [Authorization, x-sso-token, x-2fa-token]

flutter:
  go_routes: 8                      # static + dynamic-generated (app_router.dart, VERIFIED)
  outbound_calls_executeWithParser: 173   # ~173 via PML native bridge (VERIFIED)
  direct_http_calls: 2              # package:http (VERIFIED)

sample_real_paths:                  # assembled in the repository layer, not in annotations
  - /aggr/user/v1/boot
  - /order/txn/v1/place/advance_order
  - /gtt-order/api/v2/gtt
EOF

echo "[generate_api_inventory] wrote $OUT (source: $SRC)"
